namespace DeepEnds.GUI
{
    using System;
    using System.Runtime.InteropServices;
    using Microsoft.VisualStudio.Shell;

    /// <summary>
    /// This class implements the tool window exposed by this package and hosts a user control.
    /// </summary>
    /// <remarks>
    /// In Visual Studio tool windows are composed of a frame (implemented by the shell) and a pane,
    /// usually implemented by the package implementer.
    /// <para>
    /// This class derives from the ToolWindowPane class provided from the MPF in order to use its
    /// implementation of the IVsUIElementPane interface.
    /// </para>
    /// </remarks>
    [Guid("38ee8b68-1a04-4c7f-a620-9c8877f1ffa4")]
    public class DeepEndsPane : ToolWindowPane
    {
        /// <summary>
        /// Initialises a new instance of the <see cref="DeepEndsPane"/> class.
        /// </summary>
        public DeepEndsPane() : base(null)
        {
            this.Caption = "DeepEnds";

            // This is the user control hosted by the tool window; Note that, even if this class implements IDisposable,
            // we are not calling Dispose on this object. This is because ToolWindowPane calls Dispose on
            // the object returned by the Content property.
            this.Content = new DeepEndsControl();
        }
    }
}
